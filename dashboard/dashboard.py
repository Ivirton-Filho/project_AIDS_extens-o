#mapa de calor
st.subheader("Mapa de calor")

if uf == "Todas":

    dados_mapa = (
        sexo_filtrado
        .groupby("uf")[["Masculino", "Feminino"]]
        .sum()
        .reset_index()
    )

    dados_mapa["casos"] = (
        dados_mapa["Masculino"]
        +
        dados_mapa["Feminino"]
    )

    geojson_url = (
        "https://raw.githubusercontent.com/"
        "codeforamerica/click_that_hood/master/"
        "public/data/brazil-states.geojson"
    )

    geojson = requests.get(
        geojson_url
    ).json()

    fig_mapa = px.choropleth(
        dados_mapa,
        geojson=geojson,
        locations="uf",
        featureidkey="properties.sigla",
        color="casos",
        hover_name="uf",
        color_continuous_scale="Reds",
        title="Casos por Estado"
    )

    fig_mapa.update_geos(
        fitbounds="locations",
        visible=False
    )

    st.plotly_chart(
        fig_mapa,
        use_container_width=True
    )

else:

    dados_municipios = (
        sexo_filtrado
        .groupby("municipio")[["Masculino", "Feminino"]]
        .sum()
        .reset_index()
    )

    dados_municipios["casos"] = (
        dados_municipios["Masculino"]
        +
        dados_municipios["Feminino"]
    )

    fig = px.bar(
        dados_municipios.sort_values(
            "casos",
            ascending=False
        ),
        x="municipio",
        y="casos",
        title=f"Casos por Município ({uf})"
    )

    st.plotly_chart(
        fig,
        use_container_width=True
    )