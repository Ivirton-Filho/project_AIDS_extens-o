import streamlit as st
import pandas as pd
import plotly.express as px
import requests

st.set_page_config(
    page_title="Dashboard Epidemiológico da AIDS",
    layout="wide"
)

st.title("Dashboard Epidemiológico da AIDS")

#lendo os dados
@st.cache_data
def carregar_dados():
    sexo = pd.read_csv("sexo.csv")
    raca = pd.read_csv("raça_cor.csv")
    idade = pd.read_csv("idade.csv")
    escolaridade = pd.read_csv("escolaridade.csv")
    ano = pd.read_csv("ano.csv")

    return sexo, raca, idade, escolaridade, ano


sexo, raca, idade, escolaridade, ano = carregar_dados()

#filtro
def aplicar_filtros(df, regiao, uf, municipio):

    df_filtrado = df.copy()

    if regiao != "Todas":
        df_filtrado = df_filtrado[
            df_filtrado["regiao"] == regiao
        ]

    if uf != "Todas":
        df_filtrado = df_filtrado[
            df_filtrado["uf"] == uf
        ]

    if municipio != "Todos":
        df_filtrado = df_filtrado[
            df_filtrado["municipio"] == municipio
        ]

    return df_filtrado

#filtros
col1, col2, col3 = st.columns(3)

#casos por região
with col1:

    regioes = sorted(
        sexo["regiao"]
        .dropna()
        .unique()
        .tolist()
    )

    regiao = st.selectbox(
        "Região",
        ["Todas"] + regioes
    )

#casos por estado
if regiao == "Todas":
    df_regiao = sexo.copy()

else:
    df_regiao = sexo[
        sexo["regiao"] == regiao
    ]

with col2:

    ufs = sorted(
        df_regiao["uf"]
        .dropna()
        .unique()
        .tolist()
    )

    uf = st.selectbox(
        "UF",
        ["Todas"] + ufs
    )

#casos por cidade
if uf == "Todas":
    df_uf = df_regiao.copy()

else:
    df_uf = df_regiao[
        df_regiao["uf"] == uf
    ]

with col3:

    municipios = sorted(
        df_uf["municipio"]
        .dropna()
        .unique()
        .tolist()
    )

    municipio = st.selectbox(
        "Município",
        ["Todos"] + municipios
    )

#aplicando os filtros em todos os gráficos
sexo_filtrado = aplicar_filtros(
    sexo,
    regiao,
    uf,
    municipio
)

raca_filtrado = aplicar_filtros(
    raca,
    regiao,
    uf,
    municipio
)

idade_filtrado = aplicar_filtros(
    idade,
    regiao,
    uf,
    municipio
)

escolaridade_filtrado = aplicar_filtros(
    escolaridade,
    regiao,
    uf,
    municipio
)

ano_filtrado = aplicar_filtros(
    ano,
    regiao,
    uf,
    municipio
)

#indicadores
total_casos = (
    sexo_filtrado["Masculino"].fillna(0).sum()
    +
    sexo_filtrado["Feminino"].fillna(0).sum()
)

masculino = (
    sexo_filtrado["Masculino"]
    .fillna(0)
    .sum()
)

feminino = (
    sexo_filtrado["Feminino"]
    .fillna(0)
    .sum()
)

c1, c2, c3 = st.columns(3)

c1.metric(
    "Total de Casos",
    f"{int(total_casos):,}"
)

c2.metric(
    "Masculino",
    f"{int(masculino):,}"
)

c3.metric(
    "Feminino",
    f"{int(feminino):,}"
)

st.divider()

#casos por ano
anos = [
    str(a)
    for a in range(2015, 2026)
]

serie_anos = {}

for coluna in anos:

    if coluna in ano_filtrado.columns:

        serie_anos[coluna] = (
            ano_filtrado[coluna]
            .fillna(0)
            .sum()
        )

df_anos = pd.DataFrame({
    "Ano": list(serie_anos.keys()),
    "Casos": list(serie_anos.values())
})

fig_ano = px.line(
    df_anos,
    x="Ano",
    y="Casos",
    markers=True,
    title="Casos por Ano"
)

st.plotly_chart(
    fig_ano,
    use_container_width=True
)

